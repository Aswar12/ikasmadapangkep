            ripple.style.left = x + 'px';
            ripple.style.top = y + 'px';
            
            setTimeout(() => ripple.remove(), 600);
        });
    });

    // Smooth scroll for coordinator cards
    const coordinatorContainer = document.querySelector('.coordinator-scroll-wrapper');
    let isDown = false;
    let startX;
    let scrollLeft;

    if (coordinatorContainer) {
        coordinatorContainer.addEventListener('mousedown', (e) => {
            isDown = true;
            startX = e.pageX - coordinatorContainer.offsetLeft;
            scrollLeft = coordinatorContainer.scrollLeft;
            coordinatorContainer.style.cursor = 'grabbing';
        });

        coordinatorContainer.addEventListener('mouseleave', () => {
            isDown = false;
            coordinatorContainer.style.cursor = 'grab';
        });

        coordinatorContainer.addEventListener('mouseup', () => {
            isDown = false;
            coordinatorContainer.style.cursor = 'grab';
        });

        coordinatorContainer.addEventListener('mousemove', (e) => {
            if (!isDown) return;
            e.preventDefault();
            const x = e.pageX - coordinatorContainer.offsetLeft;
            const walk = (x - startX) * 2;
            coordinatorContainer.scrollLeft = scrollLeft - walk;
        });
    }

    // Enhanced member search functionality
    const createSearchInput = () => {
        const searchContainer = document.createElement('div');
        searchContainer.className = 'search-container mb-4';
        searchContainer.innerHTML = `
            <div class="search-wrapper">
                <i class="fas fa-search search-icon"></i>
                <input type="text" class="form-control search-input" placeholder="Cari nama anggota atau angkatan...">
                <button type="button" class="btn btn-sm btn-outline-secondary clear-search">
                    <i class="fas fa-times"></i>
                </button>
            </div>
        `;
        
        const tabContent = document.querySelector('#departmentTabContent');
        if (tabContent) {
            tabContent.parentNode.insertBefore(searchContainer, tabContent);
            
            const searchInput = searchContainer.querySelector('.search-input');
            const clearButton = searchContainer.querySelector('.clear-search');
            
            searchInput.addEventListener('input', handleSearch);
            clearButton.addEventListener('click', () => {
                searchInput.value = '';
                handleSearch({ target: { value: '' } });
            });
        }
    };

    const handleSearch = (e) => {
        const searchTerm = e.target.value.toLowerCase();
        const allMembers = document.querySelectorAll('.member-list li');
        const allTabs = document.querySelectorAll('.tab-pane');
        
        allMembers.forEach(member => {
            const text = member.textContent.toLowerCase();
            if (text.includes(searchTerm)) {
                member.style.display = 'flex';
                member.classList.add('highlight');
            } else {
                member.style.display = 'none';
                member.classList.remove('highlight');
            }
        });
        
        // Show all tabs if searching
        if (searchTerm.length > 0) {
            allTabs.forEach(pane => {
                pane.classList.add('show', 'active');
            });
        } else {
            // Reset to original tab state
            allTabs.forEach((pane, index) => {
                if (index === 0) {
                    pane.classList.add('show', 'active');
                } else {
                    pane.classList.remove('show', 'active');
                }
            });
        }
    };

    // Initialize search
    createSearchInput();

    // Intersection Observer for animations
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };

    const observer = new IntersectionObserver(function(entries) {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }
        });
    }, observerOptions);

    // Observe all animated elements
    document.querySelectorAll('.org-card, .coordinator-group-card, .department-card').forEach(el => {
        el.style.opacity = '0';
        el.style.transform = 'translateY(30px)';
        el.style.transition = 'all 0.6s ease-out';
        observer.observe(el);
    });

    // Enhanced hover effects for cards
    document.querySelectorAll('.org-card').forEach(card => {
        card.addEventListener('mouseenter', function() {
            this.style.transform = 'translateY(-8px) scale(1.02)';
        });
        
        card.addEventListener('mouseleave', function() {
            this.style.transform = 'translateY(0) scale(1)';
        });
    });

    // Auto-scroll hint for coordinator section
    const coordinatorScrollWrapper = document.querySelector('.coordinator-scroll-wrapper');
    if (coordinatorScrollWrapper) {
        // Add subtle animation to indicate scrollability
        setTimeout(() => {
            coordinatorScrollWrapper.scrollTo({
                left: 50,
                behavior: 'smooth'
            });
            setTimeout(() => {
                coordinatorScrollWrapper.scrollTo({
                    left: 0,
                    behavior: 'smooth'
                });
            }, 1000);
        }, 2000);
    }
});
</script>

